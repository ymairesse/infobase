<div id="texte"><h2>Notion de fichier</h2>
<div class="info"><p>Dans les années 1960, les supports
magnétiques (disques durs, disquettes,&nbsp;...) étaient
encore très chers. D'autres méthodes ont
été utilisées pour stocker les informations
fournies aux ordinateurs.&nbsp;</p><p>Nous allons maintenant étudier
cette
notion de "&nbsp;fichier&nbsp;".</p><p><img src="images/punchcard.jpg"
 class="imagegauche" alt="Carte perforée" height="155" width="360"></p><p>Dans
les premiers temps
de l'informatique, les informations étaient enregistrées
sur des cartes perforées.
</p><p>De telles cartes comportaient 80 colonnes dans lesquelles on
pouvait
coder les valeurs 0 et 1 en perforant la carte ou en la laissant
intacte.</p>
<p>Sur une carte perforée, on pouvait donc enregistrer 80 octets.</p>
<p style="clear: both;">Pour enregistrer des
quantités plus grandes d'informations, il suffit alors
d'utiliser un grand nombre de ces cartes. On obtient alors un paquet de
fiches plus ou moins épais qui constitue un "<strong>fichier
informatique</strong>".</p>
<p><img src="images/fichier.jpg" alt="Fichier informatique"
 class="imagedroite" height="141" width="260">Ces fichiers contenaient
donc:</p><ul><li> <strong>les données </strong>à
faire traiter par l'ordinateur ou</li><li><strong>les programmes</strong>
à faire exécuter.</li></ul>
<p>Plus tard, des supports magnétiques (des bandes, des disques, des
disquettes) sont devenus disponibles.</p><p>Quand les supports
magnétiques ont été
utilisés plus fréquemment, on a continué d'appeler
"&nbsp;fichier informatique&nbsp;" tous ces paquets d'informations
enregistrées.</p></div>
<div class="encadre attention"> Un fichier informatique est donc
n'importe quel
ensemble d'informations (données à traiter,
résultats du traitement, programme) enregistré sur un
support quelconque.</div>
