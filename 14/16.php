<div id="texte"><h2>Rechercher un fichier</h2>
<div class="info"><p>Il est parfois nécessaire de retrouver un
fichier ou
plusieurs fichiers égarés sur un lecteur du <strong>Poste de
Travail</strong>.</p><p>Le système d'exploitation offre une possibilité
de rechercher ce ou ces
fichiers en utilisant divers critères de recherches.</p><p>Deux
possibilités existent:</p>
<ul><li><img style="width: 470px; height: 189px;"
 alt="Rechercher des fichiers ou des dossiers"
 src="images/recherche1.png" class="imagedroite">Clique sur le bouton <strong>Démarrer</strong>&nbsp;;</li><li>Sélectionne
la commande <strong>Rechercher</strong>&nbsp;;</li><li>Choisis
l'option <strong>Des fichiers ou des dossiers.</strong></li></ul>
<hr style="clear: both; visibility: hidden;"><ul><li><img
 src="images/recherche2.png" class="imagedroite" height="236"
 width="297">Clique du bouton droit sur le <strong>Poste
de Travail</strong>&nbsp;;</li><li>Sélectionne la commande <strong>Rechercher.</strong></li></ul>
<p style="clear: right;">Dans les deux cas, la boîte de dialogue
de
recherche des fichiers apparaît:</p>
<p style="text-align: center;"><img src="images/recherche3.png"
 height="508" width="646"></p><ul><li>Dans la zone <strong>Rechercher
les fichiers ou les dossiers
nommés</strong>, indique le nom du fichier
recherché&nbsp;;</li></ul>
<p>Si tu ne connais pas précisément le nom du fichier
recherché, tu
peux utiliser des caractères "joker":</p>
<ul><ul><li>le caractère <strong>*</strong> peut remplacer
n'importe
quelle suite de caractères dans le nom ou l'extension du fichier</li><li>le
caractère <strong>?</strong> peut remplacer un et un
seul
caractère dans le nom ou l'extension du fichier.</li></ul></ul>
<p>Par exemple:</p>
<span class="label">*.doc</span> tous les fichiers dont l'extension est
.doc, quel que soit leur nom&nbsp;;<br>
<span class="label">pytha*.*</span>tous les fichiers dont le nom
commence
par pytha<br>
<span class="label">fichier.do?</span>tous les fichiers dont
l'extension
commence par "do" et se termine par n'importe quel signe (.doc, .dot,
.dos, ...)<br>
<span class="label">p?thagor.*</span>tous les fichiers dont le nom est
"pythagore" ou même "pithagore" (si tu as oublié
l'orthographe de son
nom).<br>
<p>Dans la zone <strong>Rechercher dans</strong>, sélectionne
le
lecteur précis éventuel dans lequel la recherche doit
avoir lieu. </p></div>
