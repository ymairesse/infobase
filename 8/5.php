la ligne de saisie; un clic sur la fl&egrave;che vers le bas permet
de faire diminuer cette valeur.</p>
<p>La ligne de saisie peut toujours &ecirc;tre
utilis&eacute;e de la mani&egrave;re classique: on peut aussi y
