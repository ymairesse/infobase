<h2>Les compteurs et les incr&eacute;menteurs.</h2>
<p class="info"><img src="images/horloge.jpg" alt="Horloge de Windows"
 align="right" height="341" hspace="10" vspace="10" width="404">Une
ligne de saisie peut &ecirc;tre munie
d'un outil qui permet d'augmenter ou de diminuer
automatiquement sa valeur. Cet outil est appel&eacute;
"&nbsp;incr&eacute;menteur&nbsp;". Une ligne de saisie
munie d'un incr&eacute;menteur est souvent
appel&eacute;e un "&nbsp;compteur&nbsp;". La grandeur de
l'augmentation ou de la diminution (1, 2, 5, 100
