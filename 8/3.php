<h2>Les boutons de commande</h2>
<p>&nbsp;<img class="imagegauche" src="images/dialog.jpg" align="left"
 height="137" width="229">Les boutons de commande sont de petits
rectangles gris qui apparaissent comme en relief à
l'écran. Ils permettent de faire
exécuter une commande par l'ordinateur.&nbsp;
</p>
<h3>Comment les utiliser?</h3>
&nbsp;Les boutons de commande sont actionnés par un clic
de la souris.
<p>Dans la plupart des cas, un des boutons de commande est <strong>plus
en relief</strong> que les autres; il s'agit du bouton
"&nbsp;<strong>par défaut</strong>&nbsp;";
cela signifie que si l'on frappe la touche
"&nbsp;Enter&nbsp;" au clavier, c'est ce
bouton-là qui sera actionné.</p>
<p>Dans l'illustration ci-dessus, le bouton "OK" est le bouton
par défaut.</p>
<h3>Exemple</h3>
<table
 style="width: 400px; text-align: left; margin-left: auto; margin-right: auto;"
 border="1" cellpadding="0" cellspacing="0">
  <tbody>
    <tr
 style="color: rgb(255, 255, 255); text-align: left; font-weight: bold;">
      <td style="background-color: rgb(0, 0, 128);"> Exemple
de bouton de commande </td>
    </tr>
    <tr>
      <td
 style="background-color: rgb(192, 192, 192); text-align: center;">
      <p style="margin: 20px;">Ne pas cliquer sur ce bouton</p>
      <p> <input value="OK" style="margin: 20px; width: 80px;"
 onclick="alert('Ne pas appuyer, on a dit!')" type="button"> </p>
      </td>
    </tr>
  </tbody>
</table>
