        <h2>
          Les barres de défilement
        </h2>
        <p class="info">
          Les barres de défilement permettent de visualiser le contenu d'un cadre trop petit pour montrer l'ensemble
          des éléments qu'il contient.
        </p>
        <p style="text-align:center">
          <img src="images/defilement.gif" height="260" hspace="10" vspace="10" width="471">
        </p> 
        <h3>
          Comment les utiliser?
        </h3>
        <p class="info">
          Les <strong>flèches de défilement</strong> permettent de faire défiler lentement le contenu de la fenêtre
          dans la direction qu'elles indiquent; on les actionne avec un clic de la souris.
        </p>
        <p style="text-align:center">
          <img src="images/defil2.gif" height="112" width="377">
        </p>
        <p>
          Les <strong>curseurs de défilement</strong> indiquent la position approximative de la fenêtre actuelle
          par rapport à l'ensemble de la fenêtre disponible; on les actionne avec un cliquer-glisser de la souris.
        </p>
        <p>
          Un clic de la souris dans la <strong>barre de défilement</strong> provoque le mouvement de son contenu d'une
          page vers la gauche, la droite, le haut ou le bas.
        </p>
        <h3>
          Remarque
        </h3>
        <p class="info">
          Une barre de défilement horizontale est souvent appelée un <b><strong>translateur</strong></b>. Une barre de
          défilement verticale est souvent appelée un <strong>ascenseur.</strong>
        </p>
