<h2>Le "spam" (ou
pourriel) </h2>
<div class="info">
<p><img src="images/spam.jpg" class="imagedroite" height="197"
 width="227">"Spam" est, à l'origine, le nom
d'une marque de conserves dont une publicité en radio consistait en la
répétition abrutissante du nom de la marque. Cette publicité a été à
l'origine d'un sketch des <a target="_blank"
 href="http://www.arobase.org/spam/comprendre-montypython.htm">Monty
Python</a> dans lequel l'un des protagoniste répète continuellement le
mot
"Spam", couvrant ainsi les voix des autres acteurs <font
 color="#0000ff"><u><a target="_blank"
 href="http://www.mailmsg.com/sounds/spam-song.wav">A écouter
(&gt;500ko)</a></u></font>.</p>
<p>Ce terme désigne maintenant le courrier, généralement publicitaire
et commercial, non désiré et qui envahit nos boîtes aux lettres
électroniques. On parle encore de "pourriel" qui&nbsp;est la
contraction de "pourri" et de "courriel".</p>
</div>
<h3>Deux sortes principales de spam</h3>
<div class="info">
<p>On
distingue souvent deux sortes de spams:</p>
<p>1. Le <strong>spam
publicitaire</strong> qui propose l'achat de médicaments, de logiciels
piratés, de copies d'articles de luxe,...</p>
<p class="questions">De
toute façon, personne ne lit tous ces trucs. Je
me demande bien quel est l'intérêt d'envoyer toutes ces
publicités.</p>
<p class="reponses">Dire que "&nbsp;personne ne lit&nbsp;", n'est pas
tout à fait vrai. Le coût de l'envoi de dizaines de milliers de
courriers publicitaires est très faible. Il suffit que quelques
personnes réagissent et passent commande pour que la
campagne soit bénéficiaire. Et ça fonctionne, puisque les spams se
multiplient.</p>
<p>2. Le <strong>spam malveillant</strong> qui incite
le destinataire à visiter un site web malintentionné.</p>
<fieldset><legend
 style="font-weight: bold; background-color: red; color: white; font-size: large;">Danger</legend>
<p>A peine arrivé sur
les sites web indiqués, on est invité à télécharger un programme: un
virus ou un <a href="7.php">Cheval de Troie</a>.</p>
<p>Si le programme est installé, l'expéditeur du spam peut alors
prendre le contrôle de ton
ordinateur.</p>
</fieldset>
</div>
<h3>Pourquoi le spam ?</h3>
<div class="info">
<p>Dans certains pays, l'envoi de courrier électronique
publicitaire est règlementé (Belgique, France, USA,...). L'envoi de
spams depuis ces pays pourrait entraîner des poursuites en Justice.</p>
<p class="questions">Conclusion: il n'y a qu'à déposer plainte pour
chaque spam reçu. Ce sera vite terminé, leur petit jeu.</p>
<p class="reponses">Pas de chance: ce sera rarement efficace. Les
spammeurs ont évidemment trouvé une parade. </p>
<p>La solution trouvée par les spammeurs est&nbsp;d'utiliser des
milliers d'ordinateurs répartis sur la planète pour envoyer leurs
courriers. Il
leur suffit de contrôler ces ordinateurs à distance et d'y implanter
des serveurs de courrier électronique.</p>
<p class="questions">Quoi?
Ils auraient des milliers de complices répartis dans le monde?</p>
<p class="reponses">Et il se pourrait que le complice, ce soit vous... </p>
<p>Pour prendre le contrôle d'un ordinateur distant, les
spammeurs&nbsp;utilisent des <strong>virus</strong> ou des <strong>vers</strong>.
Ceux-ci ouvrent des ports des
ordinateurs qu'ils infectent. Il ne leur reste plus&nbsp;qu'à
détecter les ordinateurs qui leur répondent pour en prendre le contrôle.</p>
<p>Un
ordinateur connecté à l'Internet subit généralement des tentatives
d'intrusion après quelques minutes.</p>
<p>C'est ainsi que ton ordinateur peut être utilisé par les spammeurs.</p>
<p class="attention">Il est donc impératif de toujours vérifier que ton
ordinateur n'a pas été ouvert par un logiciel malveillant.&nbsp;Ta
connexion serait donc ralentie et tu risques de recevoir
des plaintes pour envoi de spam.</p>
</div>
<h3>La loi en Belgique </h3>
<div class="info">
<p>Selon l'article 14 § 1er, de la loi du 11 mars
2003,
l'envoi de spam est interdit en Belgique.</p>
<blockquote>
  <p><strong> L'utilisation du courrier électronique à des
fins de
publicité est interdite, sans le consentement préalable, libre,
spécifique et informé du destinataire des messages. </strong></p>
</blockquote>
<p>Le texte de cette loi est disponible sur <a
 href="http://www.juridat.be/cgi_loi/loi_a.pl?language=fr&amp;caller=list&amp;cn=2003031132&amp;la=f&amp;fromtab=loi&amp;sql=dt=%27loi%27&amp;tri=dd+as+rank&amp;rech=1&amp;numero=1"
 target="_blank">le site web officiel du Ministère de la Justice</a> et
commentée en langage accessible aux non-juristes sur <a
 href="http://mineco.fgov.be/information_society/spamming/spam_fr.htm#P15_428"
 target="_blank">le site web du Ministère des Affaires Economiques</a>.</p>
</div>
