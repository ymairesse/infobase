<p> En informatique, un "Cheval de Troie" (on dit aussi "Troyen" ou
"Trojan" en anglais) est un logiciel malveillant qui se présente
comme un programme utile ou une application intéressante.</p>
<p>Une fois entré dans l'ordinateur, le "Cheval de Troie" peut
commencer son travail de destruction.</p>
</fieldset><h3> Les "&nbsp;backdoors&nbsp;" </h3>
<div class="info"><p> Certains "Chevaux de Troie" ont un comportement
encore plus proche de celui de l'antique Cheval. Après être entrés dans
l'ordinateur, ils ouvrent des " portes " qui pourront ensuite être
utilisées par d'autres logiciels malveillants ou par des pirates.</p>
<p> Toute communication entre votre ordinateur et le monde extérieur
passe par l'intermédiaire de "&nbsp;portes&nbsp;" (en jargon
informatique, on parle aussi de "&nbsp;ports&nbsp;"). </p>
<ul> <li>Le trafic vers le Web passe par le port 80. </li> <li>Le
trafic du courriel passe par les ports 25 (courrier sortant)
et 110 (courrier entrant). </li> <li>Le trafic MSN-Messenger passe
par le port 1863. </li> <li>... </li>
</ul><p> Pour pénétrer dans votre ordinateur, il suffit d'ouvrir un
"&nbsp;port&nbsp;" non utilisé. Dès qu'un "&nbsp;port&nbsp;" est
ouvert, il est possible de prendre entièrement le contrôle de votre
machine depuis n'importe quel ordinateur connecté à l'Internet. </p>
<p> Ce type de "&nbsp;Chevaux de Troie&nbsp;" est encore appelé
"&nbsp;Backdoor&nbsp;" en anglais. Pour les raisons que l'on devine.</p>
</div><fieldset><legend
 style="font-weight: bold; background-color: red; color: white;">À
retenir</legend>Un "backdoor" est un logiciel de type "Cheval de Troie"
qui ouvre les portes de l'ordinateur infecté. Il permet ainsi l'entrée
d'autres logiciels malveillants ou non souhaités.</fieldset>
