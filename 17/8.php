<div id="texte">
<h2>Autres nuisances logicielles</h2>
<div class="info">
<p>Les virus, vers et Chevaux de Troie constituent des
nuisances importantes.</p>
<p>D'autres types de logiciels, dont le but premier n'est pas de se
propager d'un ordinateur à l'autre peuvent encore être ajoutés à la
liste des problèmes possibles.</p>
<p>Nous envisagerons ici d'évoquer les spywares, adwares, keyloggers et
dialers.</p>
</div>
<h3>Les spywares (ou espiogiciels) </h3>
<div class="info">
<p>Comme leur nom l'indique, les spywares sont des
logiciels dont l'objectif&nbsp;est d'espionner.</p>
<p>Le spyware est un logiciel ou un composant d'un logiciel qui
collecte
des informations sur l'utilisateur d'un ordinateur et les envoie vers
son concepteur ou un commanditaire.</p>
<p class="attention">Vous visitez tel site web, vous vous attardez sur
telle page qui présente tel article en vente. Le spyware en prend bonne
note et envoie ces informations vers un serveur.<br>
Un peu plus tard, vous travaillez calmement sur votre ordinateur, quand
une publicité pour un produit similaire apparaît. Sans que vous ayez
rien demandé. Vous fermez la fenêtre publicitaire. Deux minutes plus
tard, elle revient.</p>
<p>Vous êtes victime du spyware.</p>
</div>
<p class="questions">Ouais ! Et il vient d'où, cet espion ? Mon
antivirus est &nbsp;bien à jour et il est connu pour être efficace.</p>
<p class="reponses">Il est bien possible que vous l'ayez laissé entrer
; vous l'avez peut-être même invité.</p>
<p class="questions">Moi, j'ai fait ça? Et quand, je vous prie?</p>
<p class="reponses">Vous vous souvenez du jour où vous avez téléchargé
ce super logiciel gratuit que vous utilisez tous les jours pour
télécharger des mp3. Vous vous rappelez de cette Méga barre d'outils
pour votre navigateur web? Gratuits, tous ces programmes, mais en
échange de l'installation du spyware, en même temps que le logiciel.</p>
<div class="info">
<p>Certains spywares sont intégrés, plus ou moins
discrètement, à des logiciels gratuits. D'autres tentent de s'installer
simplement lors de la visite d'une page web.</p>
</div>
<br>
<fieldset><legend
 style="font-weight: bold; background-color: red; color: white;">À
retenir</legend>
<p> En informatique, un "spyware" est un logiciel malveillant qui
espionne le contenu d'un ordinateur ou les adresses des sites web
visités. Ces informations sont transmises à un serveur qui peut alors
envoyer des publicités ciblées.</p>
</fieldset>
<h3>Les adwares (ou pubgiciel)</h3>
<div class="info">
<p>Les " adwares " sont des logiciels du même
type que les spywares. Ils s'installent généralement sans que
l'utilisateur ait bien pris conscience du fait qu'il installe un tel
logiciel.</p>
<p> Ces logiciels ajoutent des publicités dans les pages web visitées
ou dans des fenêtres séparées.</p>
<p>A la différence des spywares, les adwares ne communiquent pas
d'information vers un serveur. Ils peuvent donc travailler même si
l'ordinateur qu'ils colonisent n'est pas connecté à l'Internet.</p>
</div>
<p class="questions">Mais, si je comprends bien, ces logiciels
fonctionnent gentiment sur *mon* ordinateur sans que je leur en aie
donné l'autorisation&nbsp;?</p>
<p class="reponses">Dans certains cas, c'est possible. Il suffit que le
niveau de sécurité du navigateur web soit trop faible. Des logiciels
peuvent alors s'installer sans prévenir.<br>
Dans d'autres cas, l'utilisateur clique trop facilement sur le bouton
qui donne son accord, sans avoir compris à quoi il s'engage.</p>
<p><img src="images/adware.jpg" height="486" width="750"></p>
<p class="reponses"> Ils utilisent des ressources de
l'ordinateur&nbsp;: occupation de mémoire, utilisation du processeur,
utilisation du disque dur,... L'ordinateur est donc ralenti. De plus,
ces programmes sont souvent mal écrits et contiennent des bugs qui font
"&nbsp;planter&nbsp;" l'ordinateur.</p>
<div class="travail">
<ul>
  <li>Recherche sur le web ce que sont les
logiciels "Cydoor" et "Gator". Présente le résultat de tes recherches
au professeur.</li>
  <li>Recherche sur le web ce que sont les BHO.</li>
</ul>
</div>
<br>
<fieldset><legend
 style="font-weight: bold; background-color: red; color: white;">À
retenir</legend>
<p> En informatique, un "adware" est un logiciel&nbsp;qui provoque
l'apparition de publicités sans devoir se connecter à un serveur.</p>
</fieldset>
<h3>Les Keyloggers (ou enregistreurs de frappes)</h3>
<div class="info">
<p><img src="images/keylogger.jpg" alt="Clavier ordinateur"
 class="imagedroite" height="140" width="200">Il ne s'agit, cette fois,
plus de publicité. Les "keyloggers" sont généralement des logiciels
commerciaux (en vente libre) qui permettent d'espionner tout ce que
fait l'utilisateur d'un ordinateur: frappes au clavier (y compris les
mots de passe, numéros de carte de crédit,...), sites web visités,
"photos" de l'écran,... Toutes les informations sont ensuite transmises
vers une adresse de courrier électronique.</p>
<p>Les "keyloggers" sont souvent présentés comme des solutions
(discutables) pour des parents qui souhaitent savoir ce que font leur
enfant ou des patrons qui désirent savoir ce que font leurs employés
lorsqu'ils sont devant leur ordinateur.</p>
<p>Certains virus ou Chevaux de Troie pourraient contenir des
"keyloogers".</p>
</div>
<fieldset><legend
 style="font-weight: bold; background-color: red; color: white;">À
retenir</legend>
<p> En informatique, un "keylogger" est un logiciel dont le rôle est de
surveiller les frappes au clavier.</p>
<p>Les informations récoltées (numéro de carte de crédit,...) peuvent
être transmises à des personnes malintentionnées.</p>
</fieldset>
<h3>Les dialers (composeurs téléphoniques)</h3>
<div class="info">
<p><img src="images/dialer.jpg" alt="Clavier téléphone"
 class="imagedroite" height="129" width="200">Un dialer peut être une
application tout à fait honnête.</p>
<p>Pour obtenir une information, pour acheter un produit ou un service
(une soluce pour un jeu d'ordinateur,...), on vous propose d'appeler un
numéro de téléphone surtaxé.</p>
<p>Le fournisseur de service peut vous proposer de télécharger un petit
logiciel qui se chargera de réaliser l'appel surtaxé.</p>
<p class="attention">Méfiance&nbsp;! Vous ne savez pas quel numéro sera
appelé par le logiciel. Il pourra s'agir d'un appel vers un numéro
surtaxé dans un pays exotique. Vous continuez de profiter du service
mais en étant connecté à un serveur situé aux antipodes.</p>
</div>
<p class="questions">Vous voulez dire qu'il pourrait me mettre deux
heures en communication téléphonique avec l'Australie?</p>
<p class="reponses">Vous avez parfaitement compris. Et la facture
risque d'être à la hauteur.</p>
<fieldset><legend
 style="font-weight: bold; background-color: red; color: white;">À
retenir</legend>
<p>Un "dialer" est un logiciel capable d'établir une communication
téléphonique à l'aide du modem de l'ordinateur.</p>
<p>Un "dialer" mal intentionné pourrait établir une communication très
onéreuse.<br>
</p>
