<h2>Autres dangers et pestes de l'Internet </h2>
<div class="info">
<p>L'utilisation de l'Internet mène à rencontrer d'autres
dangers et inconvénients. Certains ont déjà été cités.</p>
</div>
<h3>Tentatives d'intrusion dans votre ordinateur </h3>
<div class="info">
<p>Il existe plusieurs raisons pour lesquelles un
" pirate " peut tenter d'entrer dans votre ordinateur et en
prendre le contrôle :&nbsp;</p>
<ul>
  <li>Utilisation de votre ordinateur pour envoyer des spams&nbsp;;</li>
  <li>Utilisation de votre ordinateur pour lancer une attaque de grande
envergure contre les serveurs d'une société précise. Votre ordinateur
deviendra donc un "&nbsp;zombie&nbsp;" qui, en compagnie de milliers
d'autres, lancera des requêtes vers un serveur connecté à l'Internet.
Sous l'afflux de trafic, le serveur deviendra inutilisable durant tout
le temps que durera l'attaque.</li>
  <li>Utilisation de votre ordinateur pour entrer dans un autre
ordinateur d'une Société ou d'un organisme d'Etat (CIA, FBI, banque,
armée,...) sans laisser de traces autres que... les vôtres.</li>
  <li>Simple amusement à l'idée de vous voir impuissant devant votre
ordinateur qui "&nbsp;travaille tout seul&nbsp;"&nbsp;: mouvements de
la souris, ouverture et fermeture de fenêtres, messages à l'écran,
ouverture du lecteur de CD,...</li>
</ul>
<p>Une seule solution&nbsp;: fermer toutes les portes.</p>
</div>
<fieldset><legend
 style="font-weight: bold; background-color: red; color: white;">À
retenir</legend>Dès qu'un logiciel malveillant a pu ouvrir une porte de
votre ordinateur, celui-ci devient généralement accessible à toutes les
personnes malintentionnées qui souhaitent y entrer.</fieldset>
