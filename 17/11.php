<p>Il s'agit souvent de courriers de type "&nbsp;chaînes&nbsp;" que
vous êtes invité à relayer vers tout votre carnet d'adresses.</p>
<p class="attention"> Il ne faut jamais renvoyer ces messages pour une
simple raison mathématique. Imaginons que chaque personne qui reçoit un
message de ce type le renvoie à 20 personnes qui se trouvent dans son
carnet d'adresses.<br>
Chaque personne en deuxième rang renvoie vers 20 destinataires. Nous
aurons ainsi 400 messages envoyés. On peut aisément compter le nombre
de messages qui transiteront sur l'Internet après quelques heures.</p>
<p>L'effet obtenu sera simplement une saturation du réseau.</p>
<p class="attention"> Ce type de message contient souvent un grand
nombre d'adresses e-mail des personnes qui l'ont déjà transmis.<br>
Lorsque le mail arrivera dans un ordinateur infecté par un virus, il se
pourrait bien que celui-ci vole les adresses et les transmette à des
spammeurs.</p>
<p>Lorsque, exceptionnellement,&nbsp;on transmet ce type de messages,
il faut toujours:</p>
<ul>
  <li>effacer toutes les adresses existantes</li>
  <li>envoyer le message aux destinataires en "copie invisible" (bcc).</li>
</ul>
</div>
<p class="questions">Oui, mais si ça peut sauver une vie&nbsp;? J'ai
reçu un courrier, pas plus tard qu'hier, qui demandait aux personnes de
groupe sanguin O de se présenter rapidement à un hôpital pour sauver un
enfant.</p>
<p class="reponses">Effectivement, qui resterait insensible à ce genre
de message&nbsp;? Serait-ce un message du genre de <a href="http://www.hoaxbuster.com/hoaxliste/hoax_message.php?idArticle=1023" target="_blank">celui-ci (cliquer pour ouvrir)</a>&nbsp;?</p>
<p class="info">Lors de la réception de ce type de courrier en chaîne,
le mieux est de vérifier l'information sur le web (par exemple, sur le
site web <a href="http://www.hoaxbuster.com" target="_blank">www.hoaxbuster.com</a>)
et d'informer éventuellement la personne qui vous a transmis le message.</p>
<p class="questions">Bon, d'accord, mais s'il y a vraiment urgence et
que je ne trouve aucune information&nbsp;? Mettons le cas d'une
disparition d'enfant...</p>
<p class="reponses">La solution de la chaîne par courrier n'est
certainement pas la meilleure. Pensons à ce courrier envoyé en 2002 par
une personne pleine de bonnes intentions et concernant la disparition
d'une jeune fille. En 2005, cette chaîne fonctionnait encore cependant
que la jeune fille était rentrée à son domicile peu de temps après sa
fugue.</p>
<p class="questions">Mais donc, je n'agis pas alors&nbsp;?</p>
<p class="reponses">Si, éventuellement, mais pas comme cela. Il serait,
par exemple, très utile que le courrier envoyé mentionne l'adresse d'un
site web où toutes les informations peuvent être trouvées. Le site web
de <a href="http://www.childfocus.be/fr/index.php?language=fr"
 target="_blank">Child Focus</a>, par exemple.</p>
<fieldset><legend
 style="font-weight: bold; background-color: red; color: white;">À
retenir</legend>Les canulars et les rumeurs polluent nos boîtes aux
lettres.<br>
Il ne faut transmettre ces courriels qu'après avoir vérifié que leur
contenu décrit bien une réalité.</fieldset>
<h3>Autre danger&nbsp;: votre comportement sur l'Internet</h3>
<div class="info">
<p>Certains comportements lors de l'utilisation de
l'Internet ne sont pas sans poser de problèmes à cause des dangers
qu'ils représentent.</p>
<ul>
  <li>Visite de certains sites web pornographiques&nbsp;: dangers pour
les adwares et les dialers.</li>
  <li>Téléchargements sur les réseaux d'échanges de fichiers&nbsp;:
danger de télécharger n'importe quelle peste&nbsp;: virus, ver,
adware,...</li>
  <li>Téléchargement de logiciels gratuits&nbsp;: ces logiciels sont
souvent payés par les publicités qu'ils afficheront sur votre écran.<br>
    <a href="http://www.framasoft.net/rubrique2.html" target="_blank">Les
logiciels libres</a>, par contre, sont souvent gratuits mais ne posent
pas ce type de problème.</li>
  <li>Utilisation de l'ordinateur sans antivirus (parfaitement à jour)
et/ou sans pare-feu.</li>
  <li>Ouverture de n'importe quel courrier électronique dont vous ne
connaissez pas l'auteur.</li>
  <li>Ouverture des pièces jointes aux courriers électroniques, même si
l'on connaît l'auteur. Un virus ou un ver peut s'auto-envoyer en volant
l'identité d'une personne que vous connaissez bien.</li>
</ul>
</div>
