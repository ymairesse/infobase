  <div class="info">
	<p>Lorsque l'on cherche des informations sur un sujet, dans une bibliothèque ordinaire, on dispose de <strong>deux alliés</strong>:</p>
	<div style="width: 50%; float: left;"><img src="images/catalogue.png" style="width: 250px; height: 184px; padding-right: 1em;" class="imagedroite" alt="catalogue">1. <strong>Le fichier catalogue</strong> de tous les livres, journaux et revues disponibles dans la bibliothèque.</div>
	<div style="width: 50%; float: left;"><img src="images/bibliothecaire.png" style="width: 128px; height: 178px; clear: both;" class="imagedroite" alt="Bibliothécaire">2. <strong>Le bibliothécaire:</strong> <strong></strong>il oriente le lecteur vers les sources d'informations qu'il connaît bien.</div>
	<p style="clear: both;">Le travail du bibliothécaire est:</p>
	<ol>
	  <li>d'indiquer tous les ouvrages disponibles dans le catalogue et de les ranger avec ordre sur les étagères;</li>
	  <li>de sélectionner et d'acheter les meilleurs ouvrages;</li>
	  <li>de conseiller les lecteurs à la recherche d'informations.</li>
	</ol>
