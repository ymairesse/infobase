  <h3 style="clear: both;">2. L'Internet n'est pas une bibliothèque ordinaire</h3>
  <div class="info">
	<p><img src="images/internet.gif" class="imagedroite" style="width: 108px; height: 350px;" alt="Méga-bibliothèque">L'Internet est un peu comme une <strong>gigantesque bibliothèque</strong> publique où l'on peut consulter tous les documents.</p>
	<fieldset class="attention"><legend>Attention</legend>Mais il existe quelques différences entre une bibliothèque et l'Internet</fieldset>
	<ol>
	  <li>La <strong>quantité d'informations disponibles</strong> est bien
	plus grande sur l'Internet
	que dans n'importe quelle bibliothèque.</li>
	  <li>Il n'y a <strong>pas de bibliothécaire</strong> et il n'existe <strong>pas de catalogue</strong>.</li>
	  <li>L<strong>es documents ne sont pas rangés</strong>: lorsqu'un document arrive, il est simplement ajouté à l'ensemble des documents existants</li>
	  <li><strong>Tout le monde peut venir</strong> déposer un document ou enlever un document qu'il avait déposé.</li>
	</ol>
  </div>
  
