<h2> Affiner le travail sur un moteur de recherche</h2>
<div class="info">
<p>Dans certains cas, les moteurs de recherche renvoient des résultats sans rapport avec le sujet qui nous intéresse. Comment éviter cela et mieux cibler la recherche?</p>
<h3>Bien choisir les mots-clés utilisés</h3>
<ul>
  <li>Avant de commencer toute recherche, <strong>éteindre l'ordinateur et trouver les mots-clés</strong> qui correspondent bien au sujet à étudier.</li>
  <li>Ne pas hésiter à utiliser des <strong>synonymes</strong> ("<strong>masse</strong> éléphant Afrique" au lieu de "<strong>poids</strong>...", par exemple).</li>
  <li>Élargir le champ de recherches avec des termes <strong>plus généraux</strong> (pour obtenir plus de résultats).</li>
  <li>Affiner les résultats en ajoutant des termes <strong>plus précis</strong>.</li>
  <li>Ne pas faire des recherches contenant moins de <strong>deux ou trois mots-clés</strong> bien choisis simultanément.</li>
  <li>Certains mots trop commun qui pourraient figurer dans une requête sont écartés par les moteurs de recherche (<strong>le, la, de, un, des</strong>, ...). <strong>Ils ne servent donc à rien.</strong> </li>
</ul>
<p>Les moteurs de recherche ne sont, à l'heure actuelle, <strong>pas vraiment conçus pour reconnaître les langues naturelles</strong>. Ni le français, ni l'anglais, ni même le chinois. <br>
<strong>Il ne sert donc à rien de poser une question en langue naturelle.</strong></p>
<h4>Varier les combinaisons de mots-clés utilisées</h4>
<p>Ne pas hésiter à faire des recherches sur des variantes de combinaisons de mots-clés.</p>
<p class="blockoftextexpli">Pour un moteur de recherche, les deux expressions suivantes seront totalement différentes&nbsp;:</p>
<ul>
  <li>hôtel côte belge</li>
  <li>vacances mer Nord</li>
</ul>
<p class="blockoftextexpli">Pour un être humain (qui connaît la Belgique), il est clair qu'elles portent quasiment sur le même sujet.</p>
<h4>Attention aux accents</h4>
<p>En principe, les caractères accentués sont sans importance. Une recherche sur "bébé phoque" donnera les mêmes résultats que la recherche sur "bebe phoque".</p>
<p>Attention toutefois si le mot existe dans une autre langue (c'est fréquent entre le français et l'anglais).</p>
<p>Faire une recherche sur le mot "elephant", par exemple, risque de ramener des résultats en anglais (ou les "e" ne sont pas accentués). Ce qui est ennuyeux si l'on ne maîtrise pas un peu cette langue.</p>
<h3>Les directives particulières</h3>
<h4>Singulier et pluriel</h4>
<p>On vérifie aisément que les recherches portant sur des mots au singulier ne donnent pas forcément les mêmes résultats que pour les mots au pluriel.</p>
<p>Exemple: faire une recherche sur "éléphant" et "éléphants". Conclure.</p>
<h4>Synonymes</h4>
<p>Si l'on manque soi-même d'idées pour trouver des synonymes aux mots-clés de la recherche, on peut demander au moteur de recherche de faire le travail à notre place.</p>
<p>Exemple: faire une recherche sur "maison" et "~maison". Conclure. Fonctionne avec <a href="http://www.google.com">Google</a>.</p>
<h4>Le joker *</h4>
<p>Pour remplacer un mot ou une partie de mot dans une recherche, on peut utiliser le caractère * qui le remplacera.</p>
<p>Exemple: faire une recherche sur "papier" et "papier *"</p>
<h3>Donner des directives aux moteurs de recherche</h3>
<h4>Recherche sur une expression</h4>
<p>Pour obliger le moteur de recherche à trouver une expression précise plutôt que les mots séparés:</p>
<ul>
  <li>mettre les mots entre des guillemets</li>
</ul>
Exemple: <strong>"Le village de Rossignol"</strong>
<h4>Exclure des mots</h4>
<p>Lorsqu'une recherche renvoie des résultats connexes qui ne sont pas souhaités, on peut demander à ce que certains mots soient évités:</p>
<ul>
  <li>mettre un signe "<strong>-</strong>" devant le mot</li>
</ul>
<p>Exemple: Je cherche des informations sur la biologie du Rossignol. Mais beaucoup de sites web évoquent le mot "Rossignol" sans aucun rapport avec les oiseaux. Je peux donc chercher</p>
<p><strong>rossignol -ski -camping -village -"centre culturel" -chambres -domaine </strong></p>
<h4>Rendre un mot obligatoire</h4>
<p>Pour n'obtenir que les résultats qui contiennent obligatoirement un mot précis:</p>
<ul>
  <li>mettre un signe "<strong>+</strong>" devant le mot</li>
</ul>
Exemple: <strong>Rossignol +oiseau</strong><br>
Exemple: <strong>Python +reptile</strong>
<h4>Rendre plusieurs mots obligatoires simultanément</h4>
<p>Pour obtenir les résultats qui contiennent en même temps plusieurs mots</p>
<ul>
  <li>&nbsp; utiliser l'opérateur "<strong>AND</strong>" (qui signifie "ET", en français)</li>
</ul>
<p>Attention, tous les moteurs de recherche n'acceptent pas cette directive. cfr <a href="http://bing.com">BING</a></p>
<p>Exemple: <strong>diamant AND émeraude</strong></p>
<h4>Laisser le choix entre plusieurs mots</h4>
<p>Pour obtenir les résultats qui contiennent l'un ou l'autre des mots,</p>
<ul>
  <li>utiliser l'opérateur "<strong>OR</strong>" (qui signifie "OU", en français)</li>
</ul>
<p>Il faut obligatoirement utiliser l'opérateur en lettre majuscules.</p>
<p>Test: Faire une recherche</p>
<ol>
  <li>sur les 3 mots-clés <strong>diamant</strong> <strong>or</strong> <strong>émeraude</strong> puis</li>
  <li><strong>diamant OR émeraude</strong></li>
</ol>

<p>Quelle différence constate-t-on?</p>
<p>Exemples:</p>
<p> <strong>cheval OR chevaux</strong></p>
<p><strong> Gand OR Gent</strong> (la ville de Belgique en français ou en néerlandais)</p>

<h4>L'ordre des termes dans la requête peut être important</h4>
<p>Les premiers mots de la requête définissent le contexte, les mots suivants précisent le domaine de recherche.</p>
<p>Exemple:</p>
<ul>
  <li>Pour chercher des informations sur un congrès de géographie se déroulant à Bruxelles, demander</li>
  <li>"géographie Bruxelles" plutôt que "Bruxelles géographie" (qui évoquera surtout la géographie de Bruxelles)</li>
</ul>
<h4>Faire une recherche dans un site web seulement</h4>
<p>Pour restreindre la recherche à un seul site web:</p>
<ul>
  <li>ajouter l'expression <strong>site:</strong>www.lesite.com aux mots-clés de la recherche</li>
</ul>
<p>Exemple: <strong>traitement texte site:sio2.be</strong></p>
<h4>Voir les sites liés à un autre</h4>
<p>Pour voir tous les sites qui ont établi un lien hypertexte vers un autre site:</p>
<ul>
  <li>ajouter l'expression <strong>link:</strong>www.lesite.com aux mots-clés de la recherche</li>
</ul>
<p>Exemple: <strong>link:sio2.be</strong></p>
