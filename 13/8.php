<h2>Évaluation d'un site web</h2>
<h3>Quelques critères d'évalution</h3>
<div class="info">
<p><?php dewplay("8-1.mp3");?>Les moteurs de recherche ne font aucune évaluation des sites web qu'ils indexent. Les "spiders" lisent tout et indexent tout. Sans faire de tri entre le bon et le moins bon.</p>
<p>Comment évaluer si un site web est fiable et si, pour moi, il correspond à ce que je peux attendre?</p>
<p>Pour répondre à ces questions, on peut utiliser la méthode "<strong>Qui - Quoi - Quand - Où - Pourquoi - Comment</strong>".</p>
</div>
<p class="questions">Oh là! Comme vous y allez. Quelques explications ne seraient pas de refus.</p>
<p class="reponses">Ce sera avec plaisir</p>
<div class="info">
<p>Pour évaluer la qualité d'un site web, nous allons nous poser ces <strong>6 questions</strong>:</p>
<h3>Qui?</h3>
<?php dewplay("8-2.mp3");?>
<ul>
  <li><img class="imagedroite" style="width: 175px; height: 225px;" alt="Qui?" src="images/qui.png">L'auteur du site web est-il identifiable? Est-ce une personne particulière ou s'agit-il d'un site d'une institution, d'un organe de presse, d'un gouvernement,...</li>
  <li>Existe-t-il un moyen d'entrer en contact avec l'auteur, une adresse mail, un formulaire,... ?</li>
</ul>
<p>S'il s'agit d'une personne particulière, quelles sont ses qualifications pour aborder le sujet? Est-ce un spécialiste dans le domaine? Est-il reconnu? Son nom est-il cité par d'autres sites web? Que dit-on de lui?</p>
<p>S'il s'agit d'un organisme ou d'une institution, son nom est-il connu? D'autres sites y font-ils référence?</p>
<p>Pour retrouver le nom et des informations sur l'auteur, il est parfois nécessaire de remonter jusqu'à la page d'accueil du site. Pour ce faire, il faut raccourcir progressivement l'adresse URL jusqu'au moment où l'on arrive à une page significative:</p>
<table style="border-width: 0pt; width: 100%; height: 77px;" border="0" cellpadding="2" cellspacing="2">
  <tbody>
    <tr>
      <td style="vertical-align: top;">http://www.site.com/dossier1/sousDossierA/sousDossierX
      </td>
      <td style="vertical-align: top;">=&gt; le nom de l'auteur est-il accessible?</td>
    </tr>
    <tr>
      <td style="vertical-align: top;">http://www.site.com/dossier1/sousDossierA/</td>
      <td style="vertical-align: top;">=&gt; le nom de l'auteur est-il accessible?</td>
    </tr>
    <tr>
      <td style="vertical-align: top;">http://www.site.com/dossier1</td>
      <td style="vertical-align: top;">=&gt; le nom de l'auteur est-il accessible?</td>
    </tr>
    <tr>
      <td style="vertical-align: top;">http://www.site.com/</td>
      <td style="vertical-align: top;">=&gt; le nom de l'auteur est-il accessible?</td>
    </tr>
  </tbody>
</table>
<p>Lorsque le site est produit par un organisme reconnu, le nom de cet organisme figure généralement dans l'adresse URL du site. Exemple: <a href="http://public.web.cern.ch/public/fr/lhc/Computing-fr.html" target="_blank">http://public.web.<span style="font-weight: bold; color: red;">cern</span>.ch/public/fr/lhc/Computing-fr.html</a>&nbsp; <br>
où l'on trouve la mention du CERN (Centre Européen pour la Recherche Nucléaire)</p>
<h3>Quoi?</h3>
<?php dewplay("8-3.mp3");?>
<ul>
  <li><img class="imagedroite" style="width: 175px; height: 122px;" alt="quoi?" src="images/quoi.png">Quelles sont les informations intéressantes que je trouve sur ce site?</li>
  <li>Les informations données sont-elles compréhensibles? Sont-elles d'un niveau trop élevé? Sont-elles d'un niveau assez élevé?</li> 
  <li>Le document apporte-t-il des informations nouvelles? S'agit-il éventuellement d'une copie d'un autre site web (plagiat)?</li>
</ul>
<p>Si les informations trouvées sont d'un niveau trop élevé par rapport à mes connaissances, je ne pourrai sans doute pas les exploiter. Si le site web est à l'usage des enfants de l'école primaire, son niveau ne sera peut-être pas suffisant.</p>
<p>Si les informations présentées me sont déjà connues, est-ce parce que je les ai déjà trouvées ailleurs? De nombreux sites se contentent de copier/coller des textes publiés ailleurs. Il faut alors retrouver l'original.</p>
<h3>Quand?</h3>
<?php dewplay("8-4.mp3");?>
<ul>
  <li><img class="imagedroite" style="width: 90px; height: 90px;" alt="quand?" src="images/calendrier.png">Quelle est la date de création du site?</li>
  <li>Quelle est la date de dernière mise à jour de la page consultée?</li>
</ul>
<p>Dans de nombreux cas, la date de publication des informations est importante. Peut-être les informations sont-elles obsolètes? L'auteur prend-il soin de remettre les informations à jour?</p> 
<h3>Où?</h3>
<?php dewplay("8-5.mp3");?>
<ul>
  <li><img class="imagedroite" style="width: 157px; height: 153px;" alt="Où?" src="images/compas.png">Pour quel endroit les informations données sont-elles pertinentes?</li>
</ul>
<p>Si je cherche les tarifs postaux pour envoyer un colis, il faut m'assurer que les tarifs que je trouve sont valables dans mon pays. Si j'habite au Luxembourg, les tarifs en vigueur au Québec ne m'intéresseront pas.</p>
<h3>Pourquoi?</h3>
<?php dewplay("8-6.mp3");?>
<ul>
  <li>Pour quelle raison l'auteur a-t-il publié ce site web? A-t-il un intérêt financier, philosophique, religieux,... ?</li>
</ul>
<p><img class="imagedroite" style="width: 175px; height: 112px;" alt="pourquoi?" src="images/masque.png">Un site web d'une société qui produit des détergents de lessive pourrait avoir un intérêt à indiquer que ses produits sont inoffensifs pour l'environnement. Le site web d'une association de défense de l'environnement mettra plutôt en évidence des arguments dans la direction inverse.<br>
Il faudra donc lire les informations données avec un esprit critique ou les écarter si l'on a des raisons de penser qu'elles peuvent ne pas être exactes et objectives.</p>
<h3>Comment?</h3>
<?php dewplay("8-7.mp3");?>
<ul>
  <li><img class="imagedroite" style="width: 175px; height: 157px;" alt="Comment?" src="images/makeup.png">La présentation du document est-elle correcte? Le texte est-il lisible? L'orthographe est-elle correcte? Les images présentées sont-elles intéressantes?</li>
</ul>
<p>Il est possible de publier des informations très intéressantes et très pertinentes sans que la forme soit respectée. Mais, généralement, les sites web de qualité respectent le fond et la forme.<br>
On accorde donc souvent une petite importance à la forme pour juger de la pertinence du fond.</p>
