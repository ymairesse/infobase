<h2> Avantages et inconvénients des outils de recherche </h2>
<div class="travail">
<p> Dans cette page, tu trouveras une liste d'avantages et
d'inconvénients pour l'utilisation des annuaires et des moteurs de
recherche. Tous les arguments sont actuellement mélangés. </p>
<p> On te demande de replacer chaque argument dans sa catégorie. </p>
<blockquote>
  <p> Par exemple, l'argument <strong>1</strong> est bien un avantage
en faveur des annuaires. J'indique donc le numéro <strong>1</strong>
dans la catégorie <strong>Avantages</strong> et du côté <strong>Annuaires</strong>.<br>
Remarque que cela a déjà été fait pour toi pour cet argument n°1. </p>
</blockquote>
<p> Fais de même pour les 9 autres arguments. Il peut y avoir trop de
cases prévues. </p>
<ol>
  <li> <font size="2">La recherche renvoie une sélection de <u>sites</u>
sur un thème précis. Dans certains cas, une courte description est
fournie (un peu comme la publicité que ferait figurer une firme dans un
annuaire téléphonique).</font> </li>
  <li> <font size="2">Les moteurs mettent leur banque de données à
jour régulièrement: le "robot" effectue régulièrement un "tour du
monde" des sites.</font> </li>
  <li> <font size="2">Le contenu des sites est sommairement vérifié
avant leur référencement pour éviter les référencements erronés.</font>
  </li>
  <li> <font size="2">Les recherches sont plus complètes car elles
portent sur l'<u>ensemble du Web</u> et non sur les seuls sites qui se
sont référencés.</font> </li>
  <li> <font size="2">Les moteurs référencent un maximum de sites,
même si le site n'a pas demandé à être référencé.</font> </li>
  <li> <font size="2">Les recherches sont moins complètes.</font> </li>
  <li> <font size="2">Un site très bien fait peut ne pas vous être
proposé si personne ne l'a référencé (tout comme un bon plombier peut
ne pas être repris ou ne pas avoir de publicité dans le bottin)</font> </li>
  <li> <font size="2">On peut être perdu sous la masse de pages
proposés par les moteurs (essayez de taper "France" dans AltaVista!).</font>
  </li>
  <li> <font size="2">Le contenu des sites n'est pas vérifié. Certains
indélicats induisent les moteurs en erreur, en proposant des mots-clés
racoleurs qui n'ont que peu de rapport avec vos centres d'intérêt.</font>
  </li>
  <li> <font size="2">Vu le processus de vérification du contenu, un
site peut n'être repris dans un annuaire qu'au bout de plusieurs
semaines (ex: Yahoo France reçoit plusieurs centaines de demandes de
référencement par jour).</font> </li>
</ol>
