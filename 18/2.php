<h3>Les antivirus "&nbsp;en ligne&nbsp;"</h3>
<div class="info"><p>Un certain nombre de sites web proposent des outils antivirus qui peuvent être utilisés " en ligne ".&nbsp;</p>
<p>Ces outils ne remplacent pas un antivirus résident. Ils permettent toutefois une vérification rapide de l'état de l'ordinateur si l'on pense avoir été infecté.</p>
<p>Les antivirus "&nbsp;en ligne&nbsp;" ne permettent pas d'empêcher l'infection virale&nbsp;; ils peuvent seulement éventuellement agir s'il est trop tard.</p>

