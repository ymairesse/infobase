    <h2>Les antivirus</h2>
<div class="info"><p><img src="images/anvitivirus.png" alt="antivirus"
 class="imagedroite" height="195" width="195">Un antivirus est un programme capable de détecter... les virus, les vers, les troyens et parfois les spywares qui peuvent infecter un ordinateur.<br>
L'antivirus devrait être résident sur l'ordinateur, mais il existe aussi des tests d'infection virale disponibles sur le web. </p>
</div>
<h3>L'antivirus résident</h3>
<div class="info">
    <p>Un antivirus " résident " est installé sur l'ordinateur comme n'importe quel autre programme classique. Il doit démarrer en même temps que l'ordinateur et rester actif durant tout le temps que dure la session de travail.&nbsp;</p> 
<p> Le logiciel antivirus devrait obligatoirement figurer sur tout ordinateur, même non connecté à l'Internet&nbsp;: les clefs USB échangées d'ordinateur à ordinateur sont également des vecteurs importants de virus.</p>
<p>Un bon antivirus doit intervenir au moment même de l'entrée ou de la tentative d'entrée d'une peste quelconque.</p>
<p>Lorsque le virus a été découvert, l'antivirus peut&nbsp;:</p>
<ul>
    <li>nettoyer les fichiers infectés en éradiquant les virus </li>
    <li>supprimer les fichiers infectés (attention aux problèmes que cela peut poser) </li>
    <li>écarter le virus dans une zone du disque dur où il ne peut nuire&nbsp;: on parle alors d'une mise en quarantaine </li>
    <li>signaler son impuissance</li>
</ul>
</div>
<div class="travail">Nous allons réaliser un petit test pour vérifier que l'antivirus de l'ordinateur que tu utilises est parfaitement actif.<br>
Avant de réaliser ce test (sans danger), tu dois obligatoirement obtenir l'accord du professeur.<br>
<ul>
    <li>Demande l'accord du professeur (indispensable).</li>
    <li>Télécharge le fichier de test <u>qui n'est pas un vrai virus</u> à l'adresse <a href="http://www.eicar.org/anti_virus_test_file.htm" target="_blank">http://www.eicar.org/anti_virus_test_file.htm</a>
-voir le lien vers le fichier <a href="http://www.eicar.org/download/eicar.com" target="_blank">eicar.com</a> ci-contre ou au bas de la page référencée où plus d'explications sont fournies (en anglais).</li>
    <li>Si l'antivirus ne réagit pas, préviens le professeur. Dans tous les autres cas, lis les informations données par ton antivirus et détruis le fichier de test; y compris la suppression de la corbeille.</li>
</ul>
</div>

<h3>Comment se procurer un logiciel antivirus&nbsp;?</h3>
<div class="info">
    <p>Les logiciels antivirus sont vendus dans le commerce. On peut se les procurer dans toutes les boutiques qui vendent du matériel informatique.</p>
    <p>Certains antivirus sont plus efficaces que d'autres. Il est bon de s'informer avant d'acheter un des produits proposés.<br>
    Parmi les critères importants, il faut considérer la
fréquence des mises à jour. Elle devrait être quotidienne.</p>
    <p>De nouveaux virus, vers et troyens apparaissent tous les jours. Les antivirus doivent "&nbsp;apprendre&nbsp;" à les reconnaître au plus vite. C'est le rôle des mises à jour. </p>

<div style="text-align: center;" class="encadre">
    Un antivirus efficace est un antivirus qui est mis à jour très régulièrement.
</div>

