<h2>Le pare-feu (ou "&nbsp;firewall&nbsp;") </h2>
<div class="info">
<p>Le pare-feu est un dispositif matériel ou logiciel dont
le rôle est d'empêcher les intrusions dans un ordinateur connecté à un
réseau et d'éviter que des informations sortent de l'ordinateur sans
l'accord de l'utilisateur. C'est donc un filtre à double-sens.&nbsp;</p>
</div>
<h3>Rôle du pare-feu</h3>
<div class="info">
<p><img src="images/firewall.jpg" alt="Firewall" class="imagedroite"
 height="263" width="231">De nombreux logiciels
malveillants sont conçus pour ouvrir des ports de manière à permettre
des intrusions. Le pare-feu empêchera l'intrusion même si un port est
ouvert. Eventuellement, le problème sera signalé à l'utilisateur.</p>
<p>Inversement, si un spyware tente d'envoyer des informations vers un
serveur distant, le pare-feu pourra agir pour empêcher ce transfert. </p>
<p>En anglais, le nom du dispositif est plus évocateur&nbsp;: on parle
de "&nbsp;firewall&nbsp;" (mur de feu). Tout ce qui se passe au-delà du
"&nbsp;firewall&nbsp;" est rendu invisible depuis l'Internet.</p>
<p class="attention">Le pare-feu n'est évidemment nécessaire que pour
les ordinateurs qui sont connectés à l'Internet ou à un réseau Intranet
non sûr. </p>
</div>
