<h2>Contre les spams </h2>
<div class="info">
<p>La lutte contre les spams passe principalement par la
prudence.</p>
<p>Un certain nombre de logiciels anti-spams sont toutefois
disponibles.
La plupart d'entre eux doivent être installés sur les
serveurs de
courrier électronique. Certains fournisseurs d'accès
à l'Internet
fournissent d'ailleurs un service anti-spam gratuit ou payant.</p>
</div>
<h3>La prudence </h3>
<div class="info">La meilleure façon de ne pas être la
victime des spams
est... de ne pas les attirer.
<ul>
  <li>Des robots parcourent sans relâche les pages du web,
à la
recherche d'adresses électroniques auxquelles il sera possible
d'envoyer des publicités.</li>
  <li>L'inscription à un service gratuit demande de fournir une
adresse
électronique valide&nbsp;: celle-ci sera peut-être
revendue.</li>
  <li>L'achat d'un article en ligne suppose de donner une adresse
électronique&nbsp;? Des courriers provenant du vendeur sont
à attendre.</li>
</ul>
</div>
<ul>
</ul>
<h3>Les solutions</h3>
<div class="info">
<ul>
  <li>Ne pas donner sa vraie adresse électronique quand ce n'est
pas
indispensable.</li>
  <li>Utiliser une adresse électronique "&nbsp;poubelle&nbsp;"
dont on
accepte qu'elle soit spammée un jour. A ce moment, on
l'abandonne.</li>
  <li>Utiliser une adresse électronique
"&nbsp;jetable&nbsp;"&nbsp;:&nbsp;<a href="http://www.jetable.org/"
 target="_blank">jetable.org</a>,&nbsp;<a
 href="http://www.spamgourmet.com/" target="_blank" class="postlink">SpamGourmet</a>,
    <a href="http://fr.trashmail.net/" target="_blank" class="postlink">Trashmail</a>,
    <a href="http://www.haltospam.com/" target="_blank" class="postlink">HaltoSpam</a>,
    <a href="http://www.mailbidon.com/" target="_blank" class="postlink">MailBidon</a>,
    <a href="http://www.kasmail.com/" target="_blank" class="postlink">Kasmail</a>,
    <a href="http://www.ephemail.net/" target="_blank" class="postlink">Ephemail</a>...</li>
  <li>Ne jamais répondre à un scam.</li>
  <li>NE JAMAIS se désinscrire lorsqu'un spam vous arrive et
vous
propose une solution de désinscription pour ne plus recevoir
d'autres
courriers.<br>
Si vous vous désinscrivez, vous indiquez par la même
occasion que votre
adresse électronique est valide. Votre adresse prend donc de la
valeur
commerciale et vous recevrez d'autant plus de spams.</li>
</ul>
<blockquote>
  <p><strong>Cette dernière remarque n'est pas forcément
valable dans les
pays où une loi anti-spam existe. En Belgique, on peut
déposer une
plainte auprès du fournisseur d'accès du spammeur si la
désinscription
n'est pas effective. Il est condamnable en Justice. Le fournisseur
d'accès peut également être poursuivi s'il n'agit
pas.</strong> </p>
</blockquote>
</div>
