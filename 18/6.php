<h2>Derniers conseils </h2>
<div class="info">
<p>Un certain nombre de conseils relatifs au comportement
à avoir avec un ordinateur ont déjà
été donnés durant cette leçon. Ajoutons-en
encore quelques-uns. </p>
</div>
<h3>Utiliser des logiciels moins sensibles </h3>
<div class="info">
<p>Les logiciels Microsoft sont extrêmement
répandus. Ils en deviennent donc des cibles
privilégiées pour les pirates et tous les logiciels
malveillants.</p>
<p>On pourra éviter une grande partie des risques en utilisant
des logiciels moins répandus que les produits Microsoft.</p>
<ul>
  <li>Le système d'exploitation: <a href="http://www.ubuntu-fr.org/"
 target="_blank">Linux</a>, <a href="http://www.apple.com/fr/macosx/"
 target="_blank">MacOS</a>,...</li>
  <li>Le navigateur web: <a
 href="http://www.mozilla-europe.org/fr/products/firefox/"
 target="_blank">Firefox</a>, <a
 href="http://www.opera.com/download/index.dml?opsys=Windows&amp;lng=fr&amp;ver=8.50&amp;platform=Windows&amp;local=y"
 target="_blank">Opera</a>,... </li>
  <li>Logiciel de courrier électronique&nbsp;: <a
 href="http://www.mozilla-europe.org/fr/products/thunderbird/"
 target="_blank">Thunderbird</a>,... </li>
</ul>
<p>Il est important de télécharger très rapidement
les mises à jour de son système d'exploitation.
Lorsqu'une faille de sécurité est trouvée, elle
est généralement très vite utilisée par les
logiciels malveillants.<br>
Les mises à jour et correctifs de Windows et d'autres logiciels
Microsoft peuvent être téléchargées depuis <a
 href="http://windowsupdate.microsoft.com/" target="_blank">http://windowsupdate.microsoft.com/</a>.<br>
Les autres systèmes d'exploitation proposent des
facilités du même type. </p>
</div>
<h3>Eviter les logiciels piratés</h3>
<div class="info">
<p>Il n'existe évidemment aucune garantie quant au
bon fonctionnement de logiciels piratés. Aucune garantie non
plus qu'ils ne contiennent pas de logiciel malveillant.</p>
<p>Ceux qui cherchent à se procurer des logiciels de
qualité sans dépenser des centaines d'Euros pour
équiper leur ordinateur se tourneront volontiers vers le
logiciel libre.<br>
Il est souvent gratuit et, le code source étant publié,
il ne contient jamais de virus ou autre peste.</p>
</div>
<h3>Etre prudent avec les logiciels gratuits</h3>
<div class="info">
<p>De nombreux logiciels gratuits n'ont cette
qualité que parce qu'ils sont rentabilisés par la
publicité qu'ils permettent.</p>
<p>Spywares et Adwares accompagnent souvent ce type de logiciels.</p>
<p>Il existe cependant de nombreux logiciels gratuits de
qualité. Il faut toujours bien s'informer avant d'installer un
tel logiciel.</p>
</div>
<h3>Eviter les téléchargements sur les réseaux
d'échanges de fichiers </h3>
<div class="info">
<p>Les réseaux d'échanges du type Kazaa ou
eMule sont extrêmement tentants pour ceux qui recherchent des
programmes, des films ou de la musique... sans débourser un Euro.</p>
<p>Malheureusement, un grand nombre de virus, vers et autres pestes y
attendent patiemment d'être téléchargés et,
à nouveau, échangés. La plus grande prudence
s'impose.</p>
</div>
