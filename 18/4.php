<h2>Contre les "adwares" et les "spywares" </h2>
<div class="info">
<p>Les "adwares" ou pubgiciels et les
"spywares" doivent pouvoir être extraits des ordinateurs
qu'ils colonisent puisqu'ils contribuent à les rendre moins stables, ou
à diffuser des informations que tout le monde n'a pas à connaître. </p>
</div>
<h3>Comment se procurer un anti-adware / anti-spyware</h3>
<div class="info">
<p>Certains logiciels de sécurité (antivirus) offrent
parfois des fonctions anti-adwares et anti-spywares. Des logiciels
spécifiques sont également téléchargeables sur l'Internet.</p>
<p class="attention">Certains adwares ou spywares sont extrêmement
agressifs et "vicieux"&nbsp;: on connaît, des produits qui
se font passer pour des anti-spywares.<br>
Ils persuadent l'utilisateur que son ordinateur est infesté de
spywares. Celui-ci télécharge alors le logiciel qui ne fait que leur
ajouter des spywares, adwares ou dialers supplémentaires.<br>
Ne pas se laisser attraper par les publicités agressives.</p>
