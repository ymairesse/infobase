</ul>
<hr style="clear: both; visibility: hidden;">
<ul>
  <li><img src="images/recherche2.png" class="imagedroite" height="236"
 width="297">Clique du bouton droit sur le <strong>Poste
de Travail</strong>&nbsp;;</li>
  <li>S&eacute;lectionne la commande <strong>Rechercher.</strong></li>
</ul>
<p style="clear: right;">Dans les deux cas, la bo&icirc;te de dialogue
