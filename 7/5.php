<h2>Quelques outils du système d'exploitation</h2>
<h3>La barre des tâches</h3>
<div class="info">
<p>Le "<strong>bureau</strong>" occupe généralement l'ensemble de
l'écran.
Sur le bureau se trouvent un certain nombre d'icônes correspondant à
des programmes que l'on peut appeler.</p>
<p> Le bas du bureau est souvent occupé par la <strong>barre des tâches</strong>.
</p>
<p align="center"> <img style="width: 800px; height: 30px;"
 alt="Barre des tâches" src="images/taches.jpg"> </p>
<p> <img style="width: 73px; height: 22px;"
 alt="Bouton de la barre des tâches" class="imagedroite"
 src="images/bouton.png" hspace="5" vspace="5">Dans la barre des
tâches, chaque programme qui est actuellement en fonctionnement est
représenté par un bouton. Il est possible de passer d'une application à
l'autre en cliquant sur ces boutons.</p>
<p>L'exemple ci-dessus provient d'un système d'exploitation Linux. </p>
<ul>
  <li>Si l'on clique sur un bouton inactif, la fenêtre du programme
correspondant apparaît alors. </li>
  <li>Si l'on clique sur un bouton actif (enfoncé), la fenêtre du
programme correspondant disparaît. </li>
</ul>
</div>
<h3> La barre de lancement rapide </h3>
<p class="info"> <img style="width: 23px; height: 22px; float: right;"
 alt="Bureau" src="images/bureau.png" hspace="3" vspace="3"><img
 style="width: 23px; height: 22px; float: right;" alt="Gimp"
 src="images/gimp.png" hspace="3" vspace="3">A côté du bouton
"Démarrer", dans la barre des tâches, un certain nombre d'icônes
peuvent être présentes. La partie de la barre des tâches qui contient
ces icônes est la <strong>barre de lancement rapide</strong>. En
cliquant sur les boutons qu'elle contient, on peut lancer une
application sans utiliser le menu "Démarrer" ni même le bureau. </p>
<h3> La zone des utilitaires et de notification </h3>
<p class="info"> <img src="images/zonenotif.png" align="right"
 height="24" hspace="5" vspace="5" width="133">Dans la partie droite de
la barre des tâches, près de l'horloge, un certain nombre d'icônes sont
également visibles. Ces icônes nous indiquent le fonctionnement de
certains utiltaires (horloge, antivirus, réglages du son,...). </p>
<p> Des icônes supplémentaires peuvent apparaître durant le travail;
par exemple, une icône représentant une imprimante, lors d'une
impression <img src="images/print.png" align="middle" height="21"
 hspace="2" vspace="2" width="66">. </p>
<p> <img src="images/notif.png" align="right" height="75" hspace="5"
 vspace="5" width="124">Des messages de service (des notifications)
provenant de l'ordinateur peuvent apparaître dans la même zone. Ces
informations sont souvent importantes et il faut les lire
attentivement. </p>
