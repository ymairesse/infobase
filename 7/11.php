<h2> Le menu de choix des applications </h2>
<div class="info"><p>Les programmes peuvent être démarrés en cliquant sur l'icône qui leur correspond sur le bureau:<br>
Exemple: pour démarrer le programme <strong>OpenOffice.org</strong>, on clique sur l'icône <img src="images/racooo.png" style="width: 64px; height: 59px;" alt="OpenOffice.org" align="middle">.</p>
<p> Les applications peuvent aussi être démarrées en passant par un menu qui permet de sélectionner l'application à démarrer.</p>
</div>
<div class="travail">
<p>Choisis le paragraphe ci-dessous qui correspond au système d'exploitation de l'ordinateur que tu utilises, Windows ou Linux.</p>
</div>
<h3>Sous Windows</h3>
<p><img src="images/demarr1.jpg" class="imagedroite" style="text-align:right; padding-left: 10" height="261" width="152">Sous Windows, le menu qui permet de choisir une application est le menu <strong>Démarrer</strong>.</p>
<p> Ce menu se trouve "&nbsp;sous&nbsp;" le bouton <span class="menu">Démarrer</span>, en bas et à gauche de l'écran. Un clic sur le bouton&nbsp;<span class="menu">Démarrer</span> provoque l'apparition d'un menu.</p>
<p> Dans ce menu, un clic sur la commande <strong>Programmes</strong> provoque l'apparition d'un nouveau menu, et ainsi de suite, en cascade.</p>
<div class="travail">
<ul>
  <li>En utilisant le menu <strong>Démarrer</strong> et les différents menus successifs, démarre les applications suivantes: </li>
  <li>Bloc-note: Démarrer &gt; Programmes &gt; Accessoires &gt; Bloc-notes </li>
  <li>Calculatrice: Démarrer &gt; Programmes &gt; Accessoires &gt; Calculatrice</li>
  <li>Détermine combien font 173 x 218 et montre au professeur comment tu as obtenu la réponse.</li>
  <li>Ferme ensuite ces applications. </li>
</ul>
</div>
<h3>Sous Linux</h3>
<p><img src="images/menuappl.jpg" class="imagedroite" style="width: 355px; height: 264px;" alt="Menu Application sous Debian/Gnome">Sous Linux, le menu de choix d'application est différent selon l'interface graphique.</p>
<p>L'exemple ci-contre à droite montre le menu <strong>Application</strong> sous Ubuntu Linux; il est généralement situé en haut et à gauche de l'écran.</p>
<p>Ci-dessous, un exemple de Linux sous l'interface <strong>KDE</strong>. Le menu de choix d'application se trouvant sous le <strong>K</strong>, en bas à gauche.</p>
<p><img class="imagegauche" src="images/kdecalc.jpg" style="width: 484px; height: 231px;" alt="Calculatrice sous KDE"></p>
<div style="clear: both;" class="travail">
<ul>
  <li>En utilisant le menu de sélection des applications, démarre les applications suivantes: </li>
  <li><strong>Gimp</strong>: dans les applications graphiques ou images. </li>
  <li><strong>Calculatrice</strong>: dans les accessoires.</li>
  <li>Détermine combien font <strong>173 x 218</strong> et montre au professeur comment tu as obtenu la réponse.</li>
  <li>Ferme ensuite ces applications. </li>
</ul>
