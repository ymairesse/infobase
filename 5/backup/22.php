        </ul>
      </div>
      <h3><a name="contextMenu"></a>La touche de menu contextuel</h3>
      <div class="info">
      <p><img src="images/context.png" alt="Menu contextuel" class="imagegauche" height="49" width="75">La touche <span class="clavier">Menu contextuel</span> est située à la droite de la barre d'espace.<br>
      Elle fait apparaître, à tout moment, un menu qui se rapporte à la situation actuelle dans l'utilisation du logiciel.</p>
      <p>Elle produit donc le même effet que le clic du bouton droit de la souris.</p>
      </div>
      <div class="travail">
        <ul>
          <li>A l'aide de la souris, sélectionne un mot dans la page actuelle.</li>
          <li>Frappe la touche <span class="clavier">Menu contextuel</span>.</li>
          <li>Observe quelques instants le menu qui est apparu.</li>
          <li>Clique dans une partie vide de la page afin de dé-sélectionner le mot choisi précédemment.</li>
          <li>Frappe à nouveau la touche <span class="clavier">Menu contextuel</span>.</li>
          <li>Le menu qui apparaît est légèrement différent.</li>
        </ul>
      </div>
