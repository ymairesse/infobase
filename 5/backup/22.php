      <p><img src="images/demarrer.png" alt="Menu Démarrer" class="imagedroite" height="357" width="394">
      <img src="images/windows.png" alt="Touche Win" class="imagegauche" height="50" width="75">Les touches <span class="clavier">Windows</span> sont situées à gauche et à droite de la barre d'espace.<br>
      Elle ne sont utiles que... lorsque le système d'exploitation utilisé est l'une des versions de Windows.</p>
      <p>Lorsque l'on frappe cette touche, le menu <span class="menu">Démarrer</span> de Windows apparaît.</p>
