          <li>A l'aide des touches de mouvement, déplace la sélection sur la commande <span class="menu">Arrêter</span></li>
          <li>Annule à l'aide de la touche <span class="clavier">Esc</span> car tu n'as pas tout à fait fini cette leçon</li>
        </ul>
