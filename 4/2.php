      <h2>Le clic de souris</h2>
      <p class="info">Quand on déplace la souris sur la table, une marque constituée généralement d'une petite flèche (<img style="width: 11px; height: 19px;" alt="pointeur de souris" src="images/pointeur.jpg">) ou d'une autre figure appelée <strong>pointeur de souris</strong> se déplace à l'écran.</p>
      <p>L'appui sur les boutons de la souris provoque différents effets, en fonction des programmes utilisés.</p>
      <p><img style="width: 258px; height: 220px;" alt="Les boutons de la souris" src="images/molette.jpg" class="imagegauche">La souris est munie de deux ou de trois boutons et, souvent, d'une molette.</p>
      <p>C'est, en général, le bouton de gauche qui est le plus utilisé. Le bouton central, quand il existe, n'est pratiquement jamais utilisé.</p>
      <p>Lorsque l'on presse le bouton de la souris, on dit que l'on "&nbsp;clique&nbsp;". Si le bouton est pressé une fois, on parle d'un "&nbsp;<strong>simple clic</strong>&nbsp;" ou d'un "&nbsp;<strong>clic</strong>&nbsp;" de la souris. Si le bouton est pressé deux fois très rapidement, on parle d'un "&nbsp;<strong>double clic</strong>&nbsp;".</p>
      <p>La <strong>molette</strong> est souvent utilisée pour faire défiler rapidement du texte à l'écran. Mais d'autres fonctions peuvent lui être attribuées (zoom, ...).</p>
