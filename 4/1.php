        <p class="info">
          <img src="images/souris.gif" alt="souris verte" class="imagegauche" height="259" width="216">La souris doit
          son nom à sa forme : c'est une petite boîte qui se meut sur la table et reliée par sa queue à l'ordinateur.
        </p>
        <p>
          <img src="images/dessous.jpg" alt="Souris" class="imagedroite" height="258" width="161">Sous le boîtier de la
          souris, une boule de caoutchouc dur qui roule sur la table communique ses mouvements à de petites roulettes.
          Ces roulettes sont elles-mêmes en relation avec l'électronique de la souris.
        </p>
        <p>
          La boule de caoutchouc a tendance à ramasser toutes les poussières qui peuvent s'accumuler sur la table ; ces
          poussières peuvent empêcher le fonctionnement de la souris. Il faut alors la nettoyer.
        </p>
        <p>
          Pour cela, il faut démonter la boule de caoutchouc et gratter les poussières accumulées sur les roulettes à
          l'aide d'un instrument pointu ou d'un coton tige très légèrement imbibé d'alcool.
        </p>
        <ol>
          <li>Retourne la souris de manière à voir la boule en caoutchouc.
          </li>
          <li>Fais tourner l'anneau dans le sens marqué par des flèches de manière à libérer la boule (attention, ceci
          n'est pas possible avec tous les modèles de souris).
          </li>
          <li>Introduis la pointe d'un crayon ou un coton tige dans le trou de manière à gratter les poussières
          accumulées sur les roulettes latérales de la souris.
          </li>
          <li>Remonte la souris.
          </li>
        </ol>
        <div class="info">
