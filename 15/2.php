<h2>Notion de fichier</h2>
<div class="info"> Dans les années 1960, les supports magnétiques
(disques durs, disquettes, ...) étaient encore très chers. D'autres
méthodes ont été utilisées pour stocker les informations fournies aux
ordinateurs.
<p><img class="imagedroite" src="images/punchcard.jpg"
 alt="Carte perforée" height="157" width="360">Les informations étaient
enregistrées sur des cartes perforées.</p>
<p>De telles cartes comportaient 80 colonnes dans lesquelles on pouvait
coder les valeurs 0 et 1 en perforant la carte ou en la laissant
intacte.</p>
<p>Sur une carte perforée, on pouvait donc enregistrer 80 octets.</p>
<p><img class="imagegauche" src="images/fichier.jpg"
 alt="Fichier informatique" height="141" width="260"></p>
<hr class="spacer">
<p>Pour enregistrer des quantités plus grandes d'informations, il
suffit alors d'utiliser un grand nombre de ces cartes. On obtient alors
un paquet de fiches plus ou moins épais qui constitue un "<strong>fichier
informatique</strong>".</p>
<p>Ces fichiers contenaient donc <strong>les données </strong>à faire
traiter par l'ordinateur ou <strong>les programmes</strong> à faire
exécuter.</p>
<p>Quand les supports magnétiques ont été utilisés plus fréquemment, on
a continué d'appeler "fichier informatique" tous ces paquets
d'informations enregistrées.</p>
</div>
<p class="encadre">Un fichier informatique est donc n'importe quel
ensemble d'informations (données à traiter, résultats du traitement,
programme) enregistré sur un support quelconque.</p>
